
public class preifxOp {

	public static void main(String[] args) {
		int num = 7;
		System.out.println(++num);    // num 출력전 값이 증가
		System.out.println(++num);    // num 출력전 값이 증가.
		System.out.println(num);
		
		System.out.println("----------------");
		
		int num2 = 7;
		System.out.println(num2++);    // num출력후에 값이 감소
		System.out.println(num2++);    // num출력후에 값이 감소 
		System.out.println(num2);

	}

}
